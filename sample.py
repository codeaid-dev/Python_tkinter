import tkinter

root = tkinter.Tk()
root.title('サンプル')
root.geometry('800x600')

cvs = tkinter.Canvas(root, width=800, height=600)
cvs.create_line(50,50,200,200,fill='red',width=5)
cvs.create_rectangle(200,50,300,200,fill='blue',outline='yellow',width=5)
cvs.create_oval(305,50,405,200,fill='green',outline='gold',width=5)
cvs.create_arc(410,50,510,200,fill='yellow',start=45,extent=270,style=tkinter.PIESLICE)
cvs.create_polygon(550,50,500,200,600,200,fill='cyan',outline='gray',width=5)
cvs.create_polygon(630,50,610,200,710,200,690,50,fill='magenta',outline='lime',width=5)

FNT = ('メイリオ',32)
l = tkinter.Label(cvs,text='ラベル：',font=FNT)
l.place(x=50,y=300)
e = tkinter.Entry(cvs,width=20,font=FNT)
e.place(x=300,y=300)
t = tkinter.Text(cvs,width=35,height=2,font=FNT)
t.place(x=50,y=360)
var = tkinter.IntVar()
var.set(1)
r1 = tkinter.Radiobutton(cvs,text='ラジオ１',font=FNT,variable=var,value=1)
r1.place(x=50,y=480)
r2 = tkinter.Radiobutton(cvs,text='ラジオ２',font=FNT,variable=var,value=2)
r2.place(x=250,y=480)
var2 = tkinter.BooleanVar()
var2.set(True)
c = tkinter.Checkbutton(cvs, text='チェックボックス',font=FNT,variable=var2)
c.place(x=450,y=480)

cvs.pack()
root.mainloop()